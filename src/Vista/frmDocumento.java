    private void cargarDatos(Documento objD) throws ParseException {
        String fecha = objD.getFechaDoc();
        String anio = fecha.substring(0, 4);
        String mes = fecha.substring(5, 7);
        String dia = fecha.substring(8, 10);
        java.util.Date fechaParseada = new SimpleDateFormat("yyyy-MM-dd").parse(anio + "-" + mes + "-" + dia);

        this.comboTipoDoc.getModel().setSelectedItem(objD.getIdTipDoc());
        this.comboOficina.getModel().setSelectedItem(objD.getIdSeccion());
        this.txtNroDoc.setText(objD.getNroDoc());
        this.txtFechaDoc.setDate(fechaParseada);
        this.txtDestino.setText(objD.getDestinatario());
        this.txtAsunto.setText(objD.getAsunto());
        this.txtNroArchivo.setText(objD.getArchivoNro());
        this.txtObvesrvacion.setText(objD.getObservaciones());
        this.txtFechaRecibe.setText(objD.getFechaRecibe());

        if (objD.getCategoria().equals("Recibida")) {
            this.txtRecibe.setSelected(true);
        } else {
            this.txtRemite.setSelected(true);
        }

        this.objO = objD.getIdSeccion();
        this.objTD = objD.getIdTipDoc();
    }

    private void validar_llenado(java.awt.event.KeyEvent evt, int opc) {
        //opc: 1 es para ingresar solo n√∫meros
        //opc: 2 es para ingresar solo letras
        char validar = evt.getKeyChar();
        if (opc == 1) {
            if (Character.isLetter(validar) && validar != KeyEvent.VK_SPACE
                    && validar != KeyEvent.VK_ENTER) {
                getToolkit().beep(); //sonido de alerta
                evt.consume();
            }
        }
        if (opc == 2) {
            if (Character.isDigit(validar) && validar != KeyEvent.VK_SPACE
                    && validar != KeyEvent.VK_ENTER) {
                getToolkit().beep(); //sonido de alerta
                evt.consume();
            }
        }
    }

    private boolean general() {
        if (this.comboTipoDoc.getSelectedIndex() == 0 || this.txtDestino.getText().trim().equals("")
                || ((JTextField) this.txtFechaDoc.getDateEditor().getUiComponent()).getText().trim().equals("")
                || this.comboOficina.getSelectedIndex() == 0 || this.txtAsunto.getText().trim().equals("")) {
            return false;

        } else {
            return true;
        }
    }

    private void nroDocObver() {
        if (this.txtNroDoc.getText().trim().equals("")) {
            this.nroDoc = "S/N";
        } else {
            this.nroDoc = this.txtNroDoc.getText().trim();
        }
        if (this.txtObvesrvacion.getText().trim().equals("")) {
            this.observacion = "S/O";
        } else {
            this.observacion = this.txtObvesrvacion.getText().trim();
        }
        if (this.txtRecibe.isSelected()) {
            this.categoria = "Recibida";
        }
        if (this.txtRemite.isSelected()) {
            this.categoria = "Remitida";
        }
        if (this.txtNroArchivo.getText().trim().equals("")) {
            this.nroArchivo = "S/N";
        } else {
            this.nroArchivo = this.txtNroArchivo.getText().trim();
        }
    }

    private void guardar() {
        if (this.txtRecibe.isSelected() || this.txtRemite.isSelected()) {
            if (this.general()) {
                this.nroDocObver();
                this.obtenerDatosCombo();
                Documento objDoc = new Documento(0, this.nroDoc, ((JTextField) this.txtFechaDoc.getDateEditor().getUiComponent()).getText(),
                        this.txtAsunto.getText(), this.txtDestino.getText(), this.txtFechaRecibe.getText(), this.nroArchivo,
                        this.observacion, this.categoria, this.objO, this.objTD, new Personal(this.id));
                try {
                    if (this.objD == null) {
                        if (DocumentoBo.grabarDocumento(objDoc, cargo)) {
                            JOptionPane.showMessageDialog(this, "Se Registr√≥ Correctamente", "MENSAJE --> DOCUMENTO", JOptionPane.INFORMATION_MESSAGE);
                            if (this.categoria.equals("Remitida")) {
                                DocumentoBo.grabarPerRecib(this.id, DocumentoBo.ultimoIdDoc(cargo), cargo);
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "No se pudo Registrar", "MENSAJE --> DOCUMENTO", JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        objDoc.setIdDocumento(this.objD.getIdDocumento());
                        if (DocumentoBo.modificarDocumento(objDoc, cargo)) {
                            JOptionPane.showMessageDialog(this, "Se Modific√≥ Correctamente", "MENSAJE --> DOCUMENTO", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(this, "No se pudo Modificar", "MENSAJE --> DOCUMENTO", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                } catch (Exception e) {
                    System.out.println(e.getMessage());
                }
                this.limpiar();
                this.limpiarBusqueda();
                this.controles(false);
                this.cargarTabla();
                this.cargarFecha();
            } else {
                JOptionPane.showMessageDialog(this, "Verifique que los campos correspondientes esten llenado correctamente.", "MENSAJE --> DOCUMENTO", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Usted no a selecionado el tipo de correspondencia.", "MENSAJE --> DOCUMENTO", JOptionPane.WARNING_MESSAGE);
        }
    }

    private void clicEditar() {
        this.comboOficinaBusc.setSelectedIndex(0);
        int fila = this.tablaDocumento.getSelectedRow();
        if (fila != -1) {
            this.objD = MTD.getDocumento(fila);
            try {
                this.cargarDatos(objD);
                this.controles(true);
                this.cargarTabla();
            } catch (ParseException ex) {
                System.out.println(ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Usted no a seleccionado un campo en la tabla.", "MENSAJE --> PERSONAL", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void clicBuscar() {
        Oficina objBusc = new Oficina();
        ArrayList<Documento> ltsDocumento = null;
        if (this.comboOficinaBusc.getItemAt(this.comboOficinaBusc.getSelectedIndex()) != null) {
            objBusc = this.comboOficinaBusc.getItemAt(this.comboOficinaBusc.getSelectedIndex());
        }
        try {
            if (this.comboOficinaBusc.getSelectedIndex() != 0 || (!((JTextField) this.txtFechaBusca.getDateEditor().getUiComponent()).getText().equals(""))) {
                if (this.comboOficinaBusc.getSelectedIndex() != 0 && ((JTextField) this.txtFechaBusca.getDateEditor().getUiComponent()).getText().equals("")) {
                    ltsDocumento = DocumentoBo.obtenerListaOficinaRecibe(objBusc.getIdOficina(), cargo);
                }
                if (this.comboOficinaBusc.getSelectedIndex() == 0 && (!((JTextField) this.txtFechaBusca.getDateEditor().getUiComponent()).getText().equals(""))) {
                    ltsDocumento = DocumentoBo.obtenerListaFechaRecpRecibe((String) ((JTextField) this.txtFechaBusca.getDateEditor().getUiComponent()).getText(), cargo);
                }
                if (this.comboOficinaBusc.getSelectedIndex() != 0 && (!((JTextField) this.txtFechaBusca.getDateEditor().getUiComponent()).getText().equals(""))) {
                    ltsDocumento = DocumentoBo.obtenerListaFechaRecpOficinaRecibe(objBusc.getIdOficina(), ((JTextField) this.txtFechaBusca.getDateEditor().getUiComponent()).getText(), cargo);
                }
                MTD.setListado_Documento(ltsDocumento);
                if (ltsDocumento.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "No se han encontrado documentos seg√∫n lo especificado.", "MENSAJE --> BUSCAR DOC.", JOptionPane.INFORMATION_MESSAGE);
                    this.cargarTabla();
                }
                this.limpiar();
                this.limpiarBusqueda();
            } else {
                JOptionPane.showMessageDialog(this, "Usted no a especificado como realizar la busqueda.", "MENSAJE --> BUSCAR DOC.", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    private void cargarFecha() {
        Calendar cal = Calendar.getInstance();
        Timestamp fecha_hora = new Timestamp(cal.getTimeInMillis());
        String fechaSistema = String.valueOf(fecha_hora);
        this.txtFechaRecibe.setText(fechaSistema.substring(0, 10));
    }

    private void limpiarBusqueda() {
        this.comboOficinaBusc.setSelectedIndex(0);
        this.txtFechaBusca.setCalendar(null);
    }
    
    private void cargarFechaCalendar(){
        Calendar cal = Calendar.getInstance();
        this.txtFechaBusca.setCalendar(cal);
        this.txtFechaDoc.setCalendar(cal);
    }
